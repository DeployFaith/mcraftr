// Minecraft 1.21 item catalog
// label = display name, id = minecraft item ID (no namespace prefix)
// maxStack = max stack size for that item

export type CatalogItem = {
  id: string
  label: string
  maxStack: number
}

export type CatalogCategory = {
  id: string
  label: string
  icon: string
  items: CatalogItem[]
}

function item(id: string, label: string, maxStack = 64): CatalogItem {
  return { id, label, maxStack }
}

